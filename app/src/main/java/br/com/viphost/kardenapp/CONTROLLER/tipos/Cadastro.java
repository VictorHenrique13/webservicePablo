package br.com.viphost.kardenapp.CONTROLLER.tipos;

public class Cadastro {
    private Boolean mesa = false;
    private Integer categoria = null;
    private Integer produto = null;

    public boolean getMesa() {
        return mesa;
    }

    public Inteiro getCategoria() {
        return new Inteiro(categoria);
    }

    public Inteiro getProduto() {
        return new Inteiro(produto);
    }
}
